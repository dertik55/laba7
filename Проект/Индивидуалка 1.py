# Ввод списка A из 10 элементов.
A = list(map(int, input("Введите 10 целых чисел через пробел: ").split()))

# Проверка количества элементов в списке.
if len(A) != 10:
    print("Неверный размер списка.")
    exit(1)

# Находим наименьший элемент и его индекс.
min_element = min(A)
min_index = A.index(min_element)

# Переставляем наименьший элемент с последним элементом.
A[min_index], A[-1] = A[-1], A[min_index]

# Вывод преобразованного списка.
print("Преобразованный список:", A)